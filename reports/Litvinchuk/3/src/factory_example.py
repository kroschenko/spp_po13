"""Реализация паттерна 'Абстрактная фабрика' для производства автомобилей."""

# pylint: disable=too-few-public-methods

from abc import ABC, abstractmethod

# ============================================================
# Абстрактные продукты — интерфейсы типов автомобилей
# ============================================================


class Sedan(ABC):
    """Интерфейс для всех седанов."""

    @abstractmethod
    def info(self):
        """Возвращает строковое описание седана."""


class SUV(ABC):
    """Интерфейс для всех внедорожников."""

    @abstractmethod
    def info(self):
        """Возвращает строковое описание внедорожника."""


# ============================================================
# Конкретные продукты Toyota
# ============================================================


class ToyotaSedan(Sedan):
    """Конкретный продукт: седан, произведённый на заводе Toyota."""

    def info(self):
        """Возвращает описание седана Toyota."""
        return "Toyota Camry — седан"


class ToyotaSUV(SUV):
    """Конкретный продукт: внедорожник, произведённый на заводе Toyota."""

    def info(self):
        """Возвращает описание внедорожника Toyota."""
        return "Toyota RAV4 — внедорожник"


# ============================================================
# Конкретные продукты BMW
# ============================================================


class BMWSedan(Sedan):
    """Конкретный продукт: седан, произведённый на заводе BMW."""

    def info(self):
        """Возвращает описание седана BMW."""
        return "BMW 5 Series — седан"


class BMWSUV(SUV):
    """Конкретный продукт: внедорожник, произведённый на заводе BMW."""

    def info(self):
        """Возвращает описание внедорожника BMW."""
        return "BMW X5 — внедорожник"


# ============================================================
# Абстрактная фабрика — интерфейс завода
# ============================================================


class CarFactory(ABC):
    """
    Абстрактная фабрика определяет методы создания
    всех типов автомобилей (седан, внедорожник).
    """

    @abstractmethod
    def create_sedan(self) -> Sedan:
        """Создаёт объект типа Sedan."""

    @abstractmethod
    def create_suv(self) -> SUV:
        """Создаёт объект типа SUV."""


# ============================================================
# Конкретные фабрики — конкретные заводы
# ============================================================


class ToyotaFactory(CarFactory):
    """Завод Toyota, производящий автомобили Toyota."""

    def create_sedan(self) -> Sedan:
        """Создаёт седан Toyota."""
        return ToyotaSedan()

    def create_suv(self) -> SUV:
        """Создаёт внедорожник Toyota."""
        return ToyotaSUV()


class BMWFactory(CarFactory):
    """Завод BMW, производящий автомобили BMW."""

    def create_sedan(self) -> Sedan:
        """Создаёт седан BMW."""
        return BMWSedan()

    def create_suv(self) -> SUV:
        """Создаёт внедорожник BMW."""
        return BMWSUV()


# ============================================================
# Клиентский код
# ============================================================


def client(factory: CarFactory):
    """
    Клиентский код, работающий с фабрикой.
    Тип фабрики определяет, какие конкретные автомобили будут созданы.
    """
    sedan = factory.create_sedan()
    suv = factory.create_suv()

    print(sedan.info())
    print(suv.info())


# ============================================================
# Пример использования
# ============================================================

if __name__ == "__main__":
    print("=== Toyota Factory ===")
    client(ToyotaFactory())

    print("\n=== BMW Factory ===")
    client(BMWFactory())
