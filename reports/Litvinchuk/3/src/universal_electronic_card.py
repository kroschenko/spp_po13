"""Реализация структурного паттерна Facade для проекта 'Универсальная электронная карта'."""

# pylint: disable=too-few-public-methods


# ============================================================
# Подсистема: Паспорт
# ============================================================


class PassportSystem:
    """Подсистема, отвечающая за паспортные данные."""

    def get_passport_info(self):
        """Возвращает паспортные данные."""
        return "Паспорт: Серия 1234 №567890, выдан МВД РФ"


# ============================================================
# Подсистема: Страховой полис
# ============================================================


class InsuranceSystem:
    """Подсистема, отвечающая за страховой полис."""

    def get_policy_info(self):
        """Возвращает данные страхового полиса."""
        return "Полис ОМС: № 1234567890, страховая компания 'Здоровье'"


# ============================================================
# Подсистема: Банковская карта
# ============================================================


class BankSystem:
    """Подсистема, отвечающая за банковские операции."""

    def pay(self, amount: float):
        """Выполняет оплату."""
        return f"Оплата {amount}₽ выполнена успешно"

    def get_balance(self):
        """Возвращает баланс банковской карты."""
        return "Баланс: 12 500₽"


# ============================================================
# Подсистема: Медицинская карта
# ============================================================


class MedicalSystem:
    """Подсистема, отвечающая за медицинские данные."""

    def get_medical_info(self):
        """Возвращает медицинские данные."""
        return "Медкарта: прививки актуальны, хронических заболеваний нет"


# ============================================================
# ФАСАД — Универсальная электронная карта
# ============================================================


class UniversalElectronicCard:
    """
    Фасад, объединяющий работу всех подсистем.
    Предоставляет простой интерфейс для пользователя.
    """

    def __init__(self):
        self.passport = PassportSystem()
        self.insurance = InsuranceSystem()
        self.bank = BankSystem()
        self.medical = MedicalSystem()

    def show_full_info(self):
        """Возвращает все данные карты в удобном виде."""
        return (
            f"{self.passport.get_passport_info()}\n"
            f"{self.insurance.get_policy_info()}\n"
            f"{self.medical.get_medical_info()}\n"
            f"{self.bank.get_balance()}"
        )

    def pay(self, amount: float):
        """Выполняет оплату через банковскую подсистему."""
        return self.bank.pay(amount)


# ============================================================
# Пример использования
# ============================================================

if __name__ == "__main__":
    card = UniversalElectronicCard()

    print("=== Информация карты ===")
    print(card.show_full_info())

    print("\n=== Оплата ===")
    print(card.pay(1500))
